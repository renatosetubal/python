movie="Jogos Mortais"
print(movie[0:]) #imprime da posicao 1 até o final

print(movie[:6]) #Pega até a posicao 5

#Buscar string da terceira até a última posicao
print(movie[2:])

#Determinando incremento
print(movie[::2])

#Pegar todas as strigs nos numeros impares
print(movie[1::2])
#Inverter String de trás para frente
print(movie[::-1])

